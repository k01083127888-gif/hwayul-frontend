// ── URL-safe slug 생성 ─────────────────────────────────────────────────────
// 예: "직장내 괴롭힘, 성추행 위자료 1억" → "직장내-괴롭힘-성추행-위자료-1억"
// - 한글/영문/숫자/언더스코어만 남기고 나머지는 하이픈으로
// - 연속 하이픈 축소, 앞뒤 하이픈 제거
// - 길이 제한 80자 (URL 너무 길어지지 않게)
export function slugify(text) {
  if (!text) return "";
  return String(text)
    .trim()
    .toLowerCase()
    // 한글·영문·숫자·언더스코어가 아닌 문자를 공백으로
    .replace(/[^\wㄱ-ㅎㅏ-ㅣ가-힣\s]/g, " ")
    // 공백을 하이픈으로
    .replace(/\s+/g, "-")
    // 연속 하이픈 하나로
    .replace(/-+/g, "-")
    // 앞뒤 하이픈 제거
    .replace(/^-|-$/g, "")
    // 80자 제한
    .slice(0, 80)
    // 혹시 끝이 하이픈이면 다시 제거
    .replace(/-+$/, "");
}
